class Demo09 {
	public static void main(String[] args) {
		
		int i = 4;
		int j = 4;
		boolean result = true;
		result = i > j;
		//-- 利用字符串进行拼接
		System.out.println("i > j Result:" + result);
		result = i >= j;
		System.out.println("Result:" + result);
		result = i < j;
		System.out.println("Result:" + result);
		result = i <= j;
		System.out.println("Result:" + result);
		result = i == j;
		System.out.println("Result:" + result);
		result = i != j;
		System.out.println("Result:" + result);
	
	}
}