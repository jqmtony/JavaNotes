class Ex05{
	public static void main(String[] args){
		//-- 输出的是字符串
		System.out.println("A");
		//-- 输出的是数字
		System.out.println(1);
		System.out.println(1.0);
		//-- 只要有双引号的就是字符串
		System.out.println("1");
		//-- 输出字符 字符使用的是单引号
		System.out.println('A');
		//-- 输出真假
		System.out.println(true);
		System.out.println(false);
	}
}