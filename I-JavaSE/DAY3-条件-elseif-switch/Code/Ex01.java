class Ex01{
	public static void main(String[] args){
		/*
			\   是转义字符
			\t  是指标符 t是Tab键的意思.
			\r
			\n
		 */
		
		System.out.println("boolean\t1字节");
		System.out.println("byte\t1字节");
		System.out.println("short\t2字节");
		System.out.println("int\t4字节");
		System.out.println("long\t8字节");
		System.out.println("double\t8字节");
		System.out.println("float\t4字节");
		System.out.println("char\t2字节");

	}
}